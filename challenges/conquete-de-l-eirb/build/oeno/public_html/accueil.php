            <h1> Les sorties</h1>

            <p> Au cours de l’année, le club oeno organise des visites des châteaux de Bordeaux tels que Loupiac ou encore St-Emilion. Nous vous emmenons jusqu’au pied du village et organisons une visite des domaines viticoles. Ceci vous permettra de déguster les différents vins du village tout en admirant ces joyaux de la région bordelaise.
            </p>
