            <h1> Les coffrets de Noël </h1>

            <p> A chaque Noël, ton club oeno t’offre la possibilité de ramener un petit souvenir de cette formidable région : un coffret de vin. Celui-ci est souvent composé de deux bouteilles, un vin rouge et un vin blanc (moelleux ou sec), très souvent de la région bordelaise. C’est une parfaite occasion pour toi d’avoir un super cadeau pour ta famille sans trop te fouler ! En plus de ça ce coffret, composé par nos soins, est très avantageux : entre 15€ et 20€. Nous te laissons suivre l’actualité du club sur Admis, sur le canal Telegram ou sur y’a boite mail avant les fêtes de fin d’année pour être sûr d’avoir un coffret, ils ne sont pas en quantité illimitée...
            </p>

