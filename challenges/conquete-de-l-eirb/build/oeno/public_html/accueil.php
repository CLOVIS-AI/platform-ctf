            <h1> Les bars à vins </h1>

            <p> Les bars à vins sont les évènements principaux du club Oeno. Tous les 6 semaines environ, le club oeno organise un bar à vin avec parfois des thèmes sur les vins où l'on peut retrouver une ambiance convivial où partager une bouteille de vin avec de la charcuterie, du fromage et un petit fond musical.
